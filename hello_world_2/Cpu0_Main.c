void task_5ms() {
    static int32_t i = 22;
    p_pin20->OUT.B.P11 = ~p_pin20->OUT.B.P11;
    cbuff_put((struct cbuff_t *)&buffer, i);
    i++;
}

void task_10ms() {
    p_pin20->OUT.B.P12 = ~p_pin20->OUT.B.P12;
}

