///////////////////////////////////////////////////////////////////////////////
// If CLK == 100Mhz:
//      10ms = 0.010, CLK = 100Mhz (ie bit-0 toogles at 0.000,000,01s, 10ns)
//      To get 10ms we need 0.01 / 0.00000001 ticks = 1000000 ticks
//      2^x = 1000000 -> xlog(2) = log(1000000) -> x = 6 / log(2) ->
//          x = 6 / 0.301 = 19.9 = 20
//      Therefore x = 20, so 2^20 is approximately 10ms toggle, so every 10ms the
//      LED should toggle
